package javaoperator;

/**
 *
 * @author yusri
 */
public class JavaOperator {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       int a=10;
       int b=12;
       int c;
       //Penjumlahan
       c=a+b;
       System.out.println(a+"+"+b+"="+c);
       //Perkalian
       c=a*b;
       System.out.println(a+"x"+b+"="+c);
       //Pembagian
       c=a/b;
       System.out.println(a+":"+b+"="+c);
       //Pembagian
       c=a-b;
       System.out.println(a+"-"+b+"="+c);
       //Sisa Pembagian
       c=a%b;
       System.out.println(a+"%"+b+"="+c);
       
    }
    
}
